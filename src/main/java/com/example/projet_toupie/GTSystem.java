package com.example.projet_toupie;

public class GTSystem {

        private String chip;
        private String weight;
        private String base;

        public GTSystem(String chip, String weight, String base) {
            this.chip = chip;
            this.weight = weight;
            this.base = base;
        }

        // Getters
        public String getChip() { return chip; }
        public String getWeight() { return weight; }
        public String getBase() { return base; }
    }


