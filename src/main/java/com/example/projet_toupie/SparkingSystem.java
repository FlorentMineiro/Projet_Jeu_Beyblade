package com.example.projet_toupie;

public class SparkingSystem {
    private String ring;
    private String chassis;

    public SparkingSystem(String ring, String chassis) {
        this.ring = ring;
        this.chassis = chassis;
    }

    public String getRing() {
        return ring;
    }

    public String getChassis() {
        return chassis;
    }
}
